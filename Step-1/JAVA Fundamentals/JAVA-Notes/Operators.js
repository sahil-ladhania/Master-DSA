/*

1. What are Operators in JAVA ?
Answer :-
* Operators in Java are symbols that perform operations on variables and values.

2. How many types of Operators are there in JAVA ?
Answer :-
* There are several types of operators in Java, including : 
    Arithmetic
    Unary
    Assignment
    Relational
    Logical
    Ternary
    Bitwise
    Shift
    Instanceof

3. What are Arithmetic Operators ?
Answer :-
* Arithmetic operators perform mathematical operations such as : 
    Addition
    Subtraction
    Multiplication
    Division
    Modulus 
* Examples include +, -, *, /, and %.

4. What are Unary Operators ?
Answer :-
* Unary operators operate on a single operand and perform various operations such as : 
    Negation 
    Increment 
    Decrement
    Logical complement
* Examples include -, ++, --, and !.

5. What are Assignment Operators ?
Answer :-
* Assignment operators are used to assign values to variables. 
* Examples include =, +=, -=, *=, /=, and %=.

6. What are Relational Operators ?
Answer :-
* Relational operators are used to compare values and determine the relationship between them.
* Examples include ==, !=, >, <, >=, and <=.

7. What are Logical Operators ?
Answer :-
* Logical operators perform logical operations such as AND, OR, and NOT. 
* Examples include &&, ||, and !.

8. What are Ternary Operators ?
Answer :-
* The ternary operator (?:) is a conditional operator that evaluates a boolean expression and returns one of two values based on the result of the evaluation.

9. What are Bitwise Operators ?
Answer :-
* Bitwise operators perform bitwise operations on integer operands at the bit level. 
* Examples include &, |, ^, ~, <<, and >>.

10. What are Shift Operators ?
Answer :-
* Shift operators perform bitwise shift operations on integer operands. 
* Examples include << (left shift) and >> (right shift).


11. What is Instance of Operator ?
Answer :-
* The instanceof operator is used to test whether an object is an instance of a particular class or interface. 
* It returns true if the object is an instance of the specified class or interface otherwise, it returns false.

*/